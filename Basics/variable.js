//variables and constants and their declaration
// Variables are containers for storing data values
// In JavaScript, there are 3 ways to declare variables: var, let, and const
// var is function scoped or globally scoped, let and const are block scoped
// var can be re-declared and updated, let can be updated but not re-declared, const cannot be updated or re-declared
// const is used to declare constants, which are variables that cannot be reassigned
//block scope means that the variable is only accessible within the block of code where it is defined
//function scope means that the variable is accessible within the function where it is defined
//global scope means that the variable is accessible throughout the entire program



